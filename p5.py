a = int(input("Введите число "))
if a % 2 == 0:
    print ("Число чётное")
else:
    print ("Число не четное")
if a%3 == 0:
    print ('Число делится без остатка на 3')
else:
    print ("Число не делится без остатка на 3")
if a**2 > 1000:
    print ('Число в квадрате больше 1000')
else:
    print ("Число в квадрате меньше 1000")
